#ifndef INCLUDED_CRT_IMAGE_CODEC
#define INCLUDED_CRT_IMAGE_CODEC

#include <CtrCodec.h>
#include <CtrPixelFormat.h>

namespace Ctr
{
    class ImageCodec : public Codec
    {
    public:
        virtual ~ImageCodec();

        class ImageData : public Codec::CodecData
        {
        public:
            ImageData():
                height(0), width(0), depth(1), size(0), 
                num_mipmaps(0), flags(0), format(PF_UNKNOWN),
                num_images(1)
            {
            }

            size_t height;
            size_t width;
            size_t depth;
            size_t size;
            uint16_t num_images;
            uint16_t num_mipmaps;
            uint32_t flags;

            PixelFormat format;

        public:
            std::string dataType() const
            {
                return "ImageData";
            }
        };

    public:
        std::string getDataType() const
        {
            return "ImageData";
        }
    };

} 

#endif
